package lthash_test

import (
	"math"
	"testing"

	"bonanza.build/pkg/crypto/lthash"

	"github.com/stretchr/testify/assert"
)

func TestHasher(t *testing.T) {
	t.Run("Empty", func(t *testing.T) {
		h := lthash.NewHasher()

		assert.Equal(
			t,
			[...]byte{
				0x46, 0x3c, 0x61, 0xad, 0x03, 0x87, 0x3a, 0xa9,
				0xe8, 0x25, 0x81, 0x20, 0x52, 0x05, 0xac, 0xc3,
				0xd3, 0xc8, 0x34, 0x6c, 0x70, 0x37, 0xc4, 0x3e,
				0x4e, 0x24, 0x1e, 0xe5, 0x29, 0xf2, 0xdc, 0x27,
			},
			h.Sum(),
		)
	})

	t.Run("HelloWorld", func(t *testing.T) {
		// Insert the strings "Hello" and "World", and validate
		// the resulting hashes.
		h := lthash.NewHasher()

		h.Add([]byte("Hello"))
		assert.Equal(
			t,
			[...]byte{
				0x39, 0x75, 0xd4, 0x12, 0x94, 0x91, 0x39, 0x37,
				0xef, 0x58, 0x34, 0xbf, 0xfb, 0x98, 0xba, 0x46,
				0x77, 0xaf, 0x76, 0x0e, 0x26, 0x72, 0xf4, 0xef,
				0x95, 0x12, 0x9c, 0x4a, 0x3d, 0x9d, 0x29, 0xca,
			},
			h.Sum(),
		)

		h.Add([]byte("World"))
		assert.Equal(
			t,
			[...]byte{
				0x7d, 0x40, 0xa3, 0xd2, 0xe5, 0x14, 0xf9, 0x17,
				0xe7, 0xa0, 0xf1, 0xf2, 0xf7, 0x49, 0xd9, 0x06,
				0xbd, 0x3e, 0x24, 0x23, 0xb5, 0x91, 0x7a, 0x88,
				0xe1, 0x89, 0x08, 0x9d, 0x96, 0x27, 0x80, 0x70,
			},
			h.Sum(),
		)
	})

	t.Run("WorldHello", func(t *testing.T) {
		// Now insert the same strings into the set the other
		// way around. The resulting hash should stay the same.
		h := lthash.NewHasher()

		h.Add([]byte("World"))
		assert.Equal(
			t,
			[...]byte{
				0x23, 0xa2, 0xc2, 0x72, 0xe1, 0xee, 0xdc, 0xbe,
				0x75, 0x12, 0x32, 0xce, 0x68, 0x4c, 0x4a, 0x75,
				0x18, 0x38, 0x3d, 0xc1, 0x6a, 0xc5, 0x2c, 0xc3,
				0xe9, 0xe6, 0x84, 0xe1, 0x33, 0xd3, 0xec, 0x69,
			},
			h.Sum(),
		)

		h.Add([]byte("Hello"))
		assert.Equal(
			t,
			[...]byte{
				0x7d, 0x40, 0xa3, 0xd2, 0xe5, 0x14, 0xf9, 0x17,
				0xe7, 0xa0, 0xf1, 0xf2, 0xf7, 0x49, 0xd9, 0x06,
				0xbd, 0x3e, 0x24, 0x23, 0xb5, 0x91, 0x7a, 0x88,
				0xe1, 0x89, 0x08, 0x9d, 0x96, 0x27, 0x80, 0x70,
			},
			h.Sum(),
		)
	})

	t.Run("Overflow", func(t *testing.T) {
		// Even though it's permitted to insert the same element
		// a small number of times, we start to see collisions
		// when it is inserted 2^16 or more times.
		h := lthash.NewHasher()

		for i := 0; i <= math.MaxUint16; i++ {
			h.Add([]byte("Hello"))
		}
		assert.Equal(
			t,
			[...]byte{
				0x46, 0x3c, 0x61, 0xad, 0x03, 0x87, 0x3a, 0xa9,
				0xe8, 0x25, 0x81, 0x20, 0x52, 0x05, 0xac, 0xc3,
				0xd3, 0xc8, 0x34, 0x6c, 0x70, 0x37, 0xc4, 0x3e,
				0x4e, 0x24, 0x1e, 0xe5, 0x29, 0xf2, 0xdc, 0x27,
			},
			h.Sum(),
		)
	})
}
