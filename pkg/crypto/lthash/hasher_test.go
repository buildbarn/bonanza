package lthash_test

import (
	"math"
	"testing"

	"bonanza.build/pkg/crypto/lthash"

	"github.com/stretchr/testify/assert"
)

func TestHasher(t *testing.T) {
	t.Run("Empty", func(t *testing.T) {
		h := lthash.NewHasher()

		assert.Equal(
			t,
			[...]byte{
				0x46, 0x3c, 0x61, 0xad, 0x03, 0x87, 0x3a, 0xa9,
				0xe8, 0x25, 0x81, 0x20, 0x52, 0x05, 0xac, 0xc3,
				0xd3, 0xc8, 0x34, 0x6c, 0x70, 0x37, 0xc4, 0x3e,
				0x4e, 0x24, 0x1e, 0xe5, 0x29, 0xf2, 0xdc, 0x27,
			},
			h.Sum(),
		)
	})

	t.Run("HelloWorld", func(t *testing.T) {
		// Insert the strings "Hello" and "World", and validate
		// the resulting hashes.
		h := lthash.NewHasher()

		h.Add([]byte("Hello"))
		assert.Equal(
			t,
			[...]byte{
				0x76, 0x25, 0xf3, 0x89, 0x50, 0x1e, 0x16, 0x04,
				0x29, 0xf9, 0x70, 0xcb, 0xb9, 0x72, 0x21, 0x1a,
				0x95, 0xe6, 0xc5, 0xc2, 0xb4, 0xd6, 0x83, 0x77,
				0xbe, 0xdc, 0x87, 0x28, 0xa9, 0xae, 0x0f, 0xce,
			},
			h.Sum(),
		)

		h.Add([]byte("World"))
		assert.Equal(
			t,
			[...]byte{
				0xac, 0x72, 0x54, 0x98, 0xb5, 0xdf, 0x08, 0xc8,
				0xbd, 0x05, 0x31, 0x6c, 0xad, 0x50, 0x28, 0xb9,
				0x59, 0x0b, 0xb0, 0xe7, 0xba, 0xcc, 0xc8, 0xbf,
				0xc8, 0x24, 0xc8, 0xdb, 0x4a, 0x7c, 0x95, 0xdf,
			},
			h.Sum(),
		)
	})

	t.Run("WorldHello", func(t *testing.T) {
		// Now insert the same strings into the set the other
		// way around. The resulting hash should stay the same.
		h := lthash.NewHasher()

		h.Add([]byte("World"))
		assert.Equal(
			t,
			[...]byte{
				0xfa, 0x19, 0x3f, 0xa1, 0xba, 0xe6, 0x54, 0x87,
				0xba, 0x5c, 0xa1, 0x5a, 0x3d, 0x76, 0xbb, 0xe6,
				0x67, 0x34, 0xa1, 0xc5, 0x2c, 0xf5, 0x28, 0x76,
				0xd1, 0xa7, 0x27, 0x8d, 0x3f, 0x60, 0xf5, 0x76,
			},
			h.Sum(),
		)

		h.Add([]byte("Hello"))
		assert.Equal(
			t,
			[...]byte{
				0xac, 0x72, 0x54, 0x98, 0xb5, 0xdf, 0x08, 0xc8,
				0xbd, 0x05, 0x31, 0x6c, 0xad, 0x50, 0x28, 0xb9,
				0x59, 0x0b, 0xb0, 0xe7, 0xba, 0xcc, 0xc8, 0xbf,
				0xc8, 0x24, 0xc8, 0xdb, 0x4a, 0x7c, 0x95, 0xdf,
			},
			h.Sum(),
		)
	})

	t.Run("Overflow", func(t *testing.T) {
		// Even though it's permitted to insert the same element
		// a small number of times, we start to see collisions
		// when it is inserted 2^16 or more times.
		h := lthash.NewHasher()

		for i := 0; i <= math.MaxUint16; i++ {
			h.Add([]byte("Hello"))
		}
		assert.Equal(
			t,
			[...]byte{
				0x46, 0x3c, 0x61, 0xad, 0x03, 0x87, 0x3a, 0xa9,
				0xe8, 0x25, 0x81, 0x20, 0x52, 0x05, 0xac, 0xc3,
				0xd3, 0xc8, 0x34, 0x6c, 0x70, 0x37, 0xc4, 0x3e,
				0x4e, 0x24, 0x1e, 0xe5, 0x29, 0xf2, 0xdc, 0x27,
			},
			h.Sum(),
		)
	})
}
