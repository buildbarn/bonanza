package encoding_test

import (
	"crypto/rand"
	"testing"

	model_encoding "bonanza.build/pkg/model/encoding"

	"github.com/buildbarn/bb-storage/pkg/testutil"
	"github.com/buildbarn/bb-storage/pkg/util"
	"github.com/stretchr/testify/require"

	"google.golang.org/grpc/codes"
	"google.golang.org/grpc/status"
)

func TestEncryptingDeterministicBinaryEncoder(t *testing.T) {
	binaryEncoder, err := model_encoding.NewEncryptingDeterministicBinaryEncoder(
		/* key = */ []byte{
			0x10, 0xc2, 0xfe, 0xfd, 0x4b, 0x11, 0x43, 0x9b,
			0x1e, 0x73, 0xda, 0x12, 0xef, 0x53, 0xd1, 0x31,
			0xf7, 0x7e, 0x6c, 0xb8, 0x2f, 0xdf, 0x79, 0xb0,
			0x90, 0x6b, 0x23, 0x3c, 0x4a, 0x32, 0xa0, 0x14,
		},
		/* additionalData = */ []byte{
			0xfb, 0x76, 0x80, 0x6e, 0xcb, 0x9d, 0x42, 0xd7,
			0xbf, 0xa0, 0x3c, 0xf6, 0x14, 0x3f, 0x0b, 0x1e,
			0xc4, 0x94, 0x5c, 0x44, 0xe0, 0xb3, 0xa7, 0xfa,
			0x2c, 0xd1, 0xe3, 0x06, 0x35, 0x55, 0xb5, 0xda,
		},
	)
	require.NoError(t, err)

	t.Run("EncodeBinary", func(t *testing.T) {
		t.Run("Empty", func(t *testing.T) {
			encodedData, tag, err := binaryEncoder.EncodeBinary(nil)
			require.NoError(t, err)
			require.Empty(t, encodedData)
			require.Equal(t, []byte{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			}, tag)
		})

		t.Run("HelloWorld", func(t *testing.T) {
			encodedData, tag, err := binaryEncoder.EncodeBinary([]byte("Hello world"))
			require.NoError(t, err)
			require.Equal(t, []byte{
				// Encrypted payload.
				0x84, 0x3e, 0x2b, 0xb2, 0xde, 0x7a, 0x97, 0x63,
				0x4c, 0x35, 0x8a,
				// Padding.
				0x6a,
			}, encodedData)
			require.Equal(t, []byte{
				0x8e, 0xe3, 0xc3, 0xff, 0xce, 0x1e, 0x6c, 0x5a,
				0xd5, 0x87, 0x8b, 0xf1, 0xde, 0x23, 0x5e, 0xf9,
			}, tag)
		})
	})

	t.Run("DecodeBinary", func(t *testing.T) {
		t.Run("Empty", func(t *testing.T) {
			decodedData, err := binaryEncoder.DecodeBinary(
				/* in = */ nil,
				/* tag = */ []byte{
					0x7e, 0x2d, 0x56, 0x18, 0xc7, 0x3b, 0xda, 0x73,
					0x0a, 0x97, 0xef, 0xfd, 0xaf, 0x6d, 0xec, 0x96,
				},
			)
			require.NoError(t, err)
			require.Empty(t, decodedData)
		})

		t.Run("HelloWorld", func(t *testing.T) {
			decodedData, err := binaryEncoder.DecodeBinary(
				/* in = */ []byte{
					// Encrypted payload.
					0x84, 0x3e, 0x2b, 0xb2, 0xde, 0x7a, 0x97, 0x63,
					0x4c, 0x35, 0x8a,
					// Padding.
					0x6a,
				},
				/* tag = */ []byte{
					0x8e, 0xe3, 0xc3, 0xff, 0xce, 0x1e, 0x6c, 0x5a,
					0xd5, 0x87, 0x8b, 0xf1, 0xde, 0x23, 0x5e, 0xf9,
				},
			)
			require.NoError(t, err)
			require.Equal(t, []byte("Hello world"), decodedData)
		})

		t.Run("DecodingParametersTooShort", func(t *testing.T) {
			_, err := binaryEncoder.DecodeBinary([]byte("Hello"), nil)
			testutil.RequireEqualStatus(t, status.Error(codes.InvalidArgument, "Decoding parameters are 0 bytes in size, while the tag was expected to be 16 bytes in size"), err)
		})

		t.Run("DecryptionFailure", func(t *testing.T) {
			_, err := binaryEncoder.DecodeBinary(
				/* in = */ []byte{
					0x4c, 0x4b, 0x7a, 0x24, 0x46, 0x08, 0x15, 0x62,
					0x3a, 0xc6, 0x45, 0x02, 0xca, 0xc3, 0xe4, 0xd3,
				},
				/* tag = */ []byte{
					0xad, 0x38, 0x0e, 0x0e, 0xfc, 0x7b, 0x2e, 0x2b,
					0x6c, 0x08, 0x61, 0xd8, 0x1d, 0x20, 0xb4, 0xc9,
				},
			)
			testutil.RequirePrefixedStatus(t, status.Error(codes.InvalidArgument, "Decryption failed: "), err)
		})

		t.Run("BadPadding", func(t *testing.T) {
			_, err := binaryEncoder.DecodeBinary(
				/* in = */ []byte{
					// Encrypted payload.
					0x1f, 0xe5, 0x5b, 0x05, 0xa7, 0x56, 0xf4, 0x90,
					0x0d, 0x6a, 0x60,

					// Padding.
					0x2c,
					// Invalid padding.
					0xce,
				},
				/* tag = */ []byte{
					0x5c, 0x4f, 0x3f, 0x1b, 0xf6, 0x42, 0x2f, 0x17,
					0x95, 0x44, 0xd3, 0x33, 0xf9, 0xea, 0x06, 0x03,
				},
			)
			testutil.RequireEqualStatus(t, status.Error(codes.InvalidArgument, "Padding contains invalid byte with value 118"), err)
		})

		t.Run("TooMuchPadding", func(t *testing.T) {
			// Additional check to ensure that other
			// implementations encode data the same way.
			// Using different amounts of padding may
			// introduce information leakage.
			_, err := binaryEncoder.DecodeBinary(
				/* in = */ []byte{
					// Encrypted payload.
					0x7c, 0x91, 0xe0, 0x2b, 0xf8, 0xfe, 0xcf, 0xd8,
					0xc6, 0xae, 0x8e,
					// Padding.
					0x4e, 0x7b,
				},
				/* tag = */ []byte{
					0x1e, 0xf5, 0xf8, 0xc3, 0xad, 0xd1, 0x80, 0x08,
					0x6f, 0x23, 0x4b, 0xeb, 0x9e, 0xeb, 0x48, 0xc3,
				},
			)
			testutil.RequireEqualStatus(t, status.Error(codes.InvalidArgument, "Encoded data is 13 bytes in size, while 12 bytes were expected for a payload of 11 bytes"), err)
		})
	})

	t.Run("RandomEncodeDecode", func(t *testing.T) {
		original := make([]byte, 10000)
		for length := 0; length < len(original); length++ {
			n, err := rand.Read(original[:length])
			require.NoError(t, err)
			require.Equal(t, length, n)

			encoded, tag, err := binaryEncoder.EncodeBinary(original[:length])
			require.NoError(t, err)

			decoded, err := binaryEncoder.DecodeBinary(encoded, tag)
			require.NoError(t, err)
			require.Equal(t, original[:length], decoded)
		}
	})

	t.Run("AppendUniqueDecodingKeys", func(t *testing.T) {
		// AppendUniqueDecodingKeys returns a list of keys that
		// should only be equal to each other if the options
		// provided to the decoder are the same.
		t.Run("Equal", func(t *testing.T) {
			require.Equal(
				t,
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0x18, 0xe3, 0x31, 0x6f, 0xd5, 0x5a, 0xf0, 0x02,
						0xdd, 0x0f, 0x84, 0xe4, 0x96, 0x3d, 0x12, 0x0f,
						0xe4, 0xd9, 0xf7, 0x44, 0xaf, 0xf9, 0xf6, 0xc5,
						0xc3, 0x60, 0x31, 0x8d, 0x7d, 0x29, 0xe4, 0x37,
					},
					/* additionalData = */ []byte{
						0x1b, 0x1b, 0x2a, 0x71, 0x52, 0xcc, 0xa2, 0x4c,
						0xdc, 0x01, 0x00, 0x39, 0x03, 0x92, 0x13, 0x14,
						0xd4, 0xbc, 0xcd, 0x2c, 0x5f, 0x13, 0xf6, 0xf4,
						0x28, 0x75, 0xe2, 0x0f, 0x3f, 0xf2, 0x1a, 0xbd,
					},
				)).AppendUniqueDecodingKeys(nil),
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0x18, 0xe3, 0x31, 0x6f, 0xd5, 0x5a, 0xf0, 0x02,
						0xdd, 0x0f, 0x84, 0xe4, 0x96, 0x3d, 0x12, 0x0f,
						0xe4, 0xd9, 0xf7, 0x44, 0xaf, 0xf9, 0xf6, 0xc5,
						0xc3, 0x60, 0x31, 0x8d, 0x7d, 0x29, 0xe4, 0x37,
					},
					/* additionalData = */ []byte{
						0x1b, 0x1b, 0x2a, 0x71, 0x52, 0xcc, 0xa2, 0x4c,
						0xdc, 0x01, 0x00, 0x39, 0x03, 0x92, 0x13, 0x14,
						0xd4, 0xbc, 0xcd, 0x2c, 0x5f, 0x13, 0xf6, 0xf4,
						0x28, 0x75, 0xe2, 0x0f, 0x3f, 0xf2, 0x1a, 0xbd,
					},
				)).AppendUniqueDecodingKeys(nil),
			)
		})

		t.Run("DifferentKey", func(t *testing.T) {
			require.NotEqual(
				t,
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0xd6, 0x1f, 0xc8, 0x28, 0x24, 0xc8, 0xb0, 0xff,
						0xac, 0x12, 0xa8, 0x82, 0x88, 0x69, 0x81, 0x21,
						0x0a, 0x62, 0x7e, 0xd7, 0x4e, 0x44, 0xd6, 0xee,
						0x3a, 0xca, 0x57, 0xce, 0xe5, 0x0d, 0x98, 0x30,
					},
					/* additionalData = */ []byte{
						0x0f, 0x4b, 0x3f, 0xa3, 0xae, 0x87, 0xc9, 0x3d,
						0x6e, 0x79, 0x67, 0x49, 0xe7, 0xa4, 0x28, 0xac,
						0xa0, 0x6d, 0x31, 0x32, 0xa7, 0xd1, 0x50, 0x5a,
						0x9f, 0x60, 0xac, 0x67, 0xca, 0xdf, 0xb3, 0xec,
					},
				)).AppendUniqueDecodingKeys(nil),
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0xf9, 0x08, 0xa8, 0x84, 0x3d, 0xf1, 0xe5, 0x6e,
						0x4c, 0xb6, 0x55, 0x24, 0xe8, 0x8b, 0xb8, 0xb5,
						0xb3, 0x3a, 0x24, 0x4c, 0xa5, 0x08, 0x15, 0x94,
						0x41, 0x6e, 0x1e, 0x71, 0x75, 0xb6, 0xb3, 0x05,
					},
					/* additionalData = */ []byte{
						0x0f, 0x4b, 0x3f, 0xa3, 0xae, 0x87, 0xc9, 0x3d,
						0x6e, 0x79, 0x67, 0x49, 0xe7, 0xa4, 0x28, 0xac,
						0xa0, 0x6d, 0x31, 0x32, 0xa7, 0xd1, 0x50, 0x5a,
						0x9f, 0x60, 0xac, 0x67, 0xca, 0xdf, 0xb3, 0xec,
					},
				)).AppendUniqueDecodingKeys(nil),
			)
		})

		t.Run("DifferentAdditionalData", func(t *testing.T) {
			require.NotEqual(
				t,
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0x91, 0x3c, 0xb6, 0x61, 0x30, 0x3c, 0x28, 0x87,
						0x6a, 0xd3, 0x65, 0x1e, 0xf6, 0x5f, 0xf9, 0x08,
						0x52, 0x74, 0x1d, 0xb8, 0xdd, 0x6e, 0x01, 0xbf,
						0x2d, 0x2e, 0x74, 0x0e, 0xe3, 0xbc, 0x86, 0xb5,
					},
					/* additionalData = */ []byte{
						0xcf, 0x5f, 0xc5, 0x1e, 0xdc, 0xb1, 0x96, 0x12,
						0x80, 0x0e, 0x80, 0x5d, 0x5e, 0xc2, 0x91, 0xde,
						0x93, 0x97, 0x65, 0x80, 0x63, 0xda, 0x4e, 0x0a,
						0x04, 0x00, 0x08, 0x81, 0x55, 0x20, 0x9a, 0xd1,
					},
				)).AppendUniqueDecodingKeys(nil),
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0x91, 0x3c, 0xb6, 0x61, 0x30, 0x3c, 0x28, 0x87,
						0x6a, 0xd3, 0x65, 0x1e, 0xf6, 0x5f, 0xf9, 0x08,
						0x52, 0x74, 0x1d, 0xb8, 0xdd, 0x6e, 0x01, 0xbf,
						0x2d, 0x2e, 0x74, 0x0e, 0xe3, 0xbc, 0x86, 0xb5,
					},
					/* additionalData = */ []byte{
						0x40, 0x27, 0x72, 0xad, 0xb2, 0x91, 0xdd, 0x82,
						0x6e, 0x8f, 0x6b, 0xa3, 0xde, 0x65, 0xc8, 0x4e,
						0xab, 0xd6, 0x90, 0x94, 0x10, 0xbd, 0x79, 0xbb,
						0x1e, 0x30, 0x18, 0xd5, 0x17, 0xd7, 0xdf, 0x23,
					},
				)).AppendUniqueDecodingKeys(nil),
			)
		})

		t.Run("DeterministicAndKeyed", func(t *testing.T) {
			// In the case of encryption, deterministic and
			// keyed decoders behave differently.
			require.NotEqual(
				t,
				util.Must(model_encoding.NewEncryptingDeterministicBinaryEncoder(
					/* key = */ []byte{
						0xd7, 0xb8, 0x5c, 0x69, 0xb0, 0x22, 0x08, 0xc1,
						0x96, 0x77, 0xcd, 0xc3, 0x10, 0x45, 0x87, 0xe0,
						0x9d, 0xe3, 0xab, 0x2b, 0x1e, 0x14, 0x41, 0xcb,
						0xe5, 0x53, 0x49, 0x2c, 0xbb, 0x96, 0xb4, 0xf2,
					},
					/* additionalData = */ []byte{
						0x98, 0xb5, 0xec, 0x4d, 0x46, 0x62, 0xd0, 0xcd,
						0x3d, 0x3f, 0x74, 0xd6, 0x89, 0x1b, 0x5c, 0x7e,
						0x61, 0xac, 0x80, 0xed, 0x4b, 0xf8, 0x50, 0xea,
						0x35, 0x58, 0x02, 0xa2, 0x19, 0x54, 0x7f, 0x4d,
					},
				)).AppendUniqueDecodingKeys(nil),
				util.Must(model_encoding.NewEncryptingKeyedBinaryEncoder(
					/* key = */ []byte{
						0xd7, 0xb8, 0x5c, 0x69, 0xb0, 0x22, 0x08, 0xc1,
						0x96, 0x77, 0xcd, 0xc3, 0x10, 0x45, 0x87, 0xe0,
						0x9d, 0xe3, 0xab, 0x2b, 0x1e, 0x14, 0x41, 0xcb,
						0xe5, 0x53, 0x49, 0x2c, 0xbb, 0x96, 0xb4, 0xf2,
					},
					/* additionalData = */ []byte{
						0x98, 0xb5, 0xec, 0x4d, 0x46, 0x62, 0xd0, 0xcd,
						0x3d, 0x3f, 0x74, 0xd6, 0x89, 0x1b, 0x5c, 0x7e,
						0x61, 0xac, 0x80, 0xed, 0x4b, 0xf8, 0x50, 0xea,
						0x35, 0x58, 0x02, 0xa2, 0x19, 0x54, 0x7f, 0x4d,
					},
				)).AppendUniqueDecodingKeys(nil),
			)
		})
	})
}
